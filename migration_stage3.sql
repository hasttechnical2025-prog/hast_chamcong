-- 10. Tạo Policies mới: CHỈ CHO PHÉP XEM (SELECT) theo JWT
-- Bảng Attendance Records: CBNV được xem lịch sử của mình, Admin/TBP được xem tất cả
CREATE POLICY "Cho phép xem records qua JWT" ON public.chamcong_attendance_records
FOR SELECT TO authenticated
USING (
  (auth.jwt() -> 'user_metadata' ->> 'type' = 'cbnv' AND employee_name = auth.jwt() -> 'user_metadata' ->> 'employee_name')
  OR
  (auth.jwt() -> 'user_metadata' ->> 'type' = 'admin')
);

-- Bảng Employees: TBP/Admin được xem toàn bộ; CBNV chỉ xem thông tin mình
CREATE POLICY "Cho phép Admin/TBP xem employees" ON public.chamcong_employees
FOR SELECT TO authenticated
USING (
  (auth.jwt() -> 'user_metadata' ->> 'type' = 'admin')
  OR
  (auth.jwt() -> 'user_metadata' ->> 'type' = 'cbnv' AND name = auth.jwt() -> 'user_metadata' ->> 'employee_name')
);

-- Bảng Attendance Logs: Chỉ Admin được xem raw logs
CREATE POLICY "Admin xem Logs" ON public.chamcong_attendance_logs
FOR SELECT TO authenticated
USING (auth.jwt() -> 'user_metadata' ->> 'type' = 'admin' AND auth.jwt() -> 'user_metadata' ->> 'role' = 'admin');

-- Bảng cấu hình và ngày lễ: Mọi user đã auth (CBNV, Admin) đều được tải cài đặt
