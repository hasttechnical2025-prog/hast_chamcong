-- 7. Tạo hàm RPC xác thực đăng nhập bảo mật trong PostgreSQL (dành cho Edge Function)
CREATE OR REPLACE FUNCTION public.chamcong_verify_auth(p_username text, p_password text)
RETURNS TABLE (id int, username text, role text, department text) AS $$
BEGIN
  RETURN QUERY
  SELECT a.id, a.username, a.role, a.department
  FROM public.chamcong_auth a
  WHERE a.username = p_username AND a.password_hash = crypt(p_password, a.password_hash);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Hàm RPC xác thực chỉ dùng mật khẩu (dành cho trang TBP)
CREATE OR REPLACE FUNCTION public.chamcong_verify_auth_password_only(p_password text)
RETURNS TABLE (id int, username text, role text, department text) AS $$
BEGIN
  RETURN QUERY
  SELECT a.id, a.username, a.role, a.department
  FROM public.chamcong_auth a
  WHERE a.password_hash = crypt(p_password, a.password_hash);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Hàm RPC cập nhật mật khẩu băm bảo mật
CREATE OR REPLACE FUNCTION public.chamcong_update_password(p_username text, p_password text)
RETURNS boolean AS $$
BEGIN
  UPDATE public.chamcong_auth
  SET password_hash = crypt(p_password, gen_salt('bf')),
      updated_at = now()
  WHERE username = p_username;
  RETURN FOUND;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

